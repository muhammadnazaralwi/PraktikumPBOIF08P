package com.nazar.praktikumpbo.pertemuan2;

public class DemoIf {
    public static void main(String[] args) {
        int x = 5;

        if(x > 0) {
            System.out.println("x adalah bilangan positif");
        } else if(x < 0) {
            System.out.println("x adalah bilangan negatif");
        } else {
            System.out.println("x adalah nol");
        }
    }
}
